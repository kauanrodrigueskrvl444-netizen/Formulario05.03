// Aqui criamos uma variável chamada "nome"
// O prompt abre uma caixinha perguntando algo pro usuário
// O valor digitado vai ser guardado dentro da variável "nome"
let nome = prompt ("Qual é o seu nome?");
// Aqui criamos outra variável chamada "corFavorita"
// Novamente usamos o prompt pra perguntar algo
// A resposta do usuário fica armazenada nessa variável
let corFavorita = prompt ("Qual é a sua cor favorita?");

alert ("Meu nome é " + nome + ", E minha cor favorita é " + corFavorita + "!");