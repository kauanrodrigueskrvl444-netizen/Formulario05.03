// Pergunta um número para o usuário
// O prompt sempre retorna TEXTO, por isso usamos Number() para converter
let numero = Number(prompt ("Digite um numero"));
// Aqui fazemos o cálculo do antecessor
// Antecessor = número - 1
let antecessor = numero - 1;
// Mostra o resultado na tela
alert ("O antecessor de " + numero + " é " + antecessor);
